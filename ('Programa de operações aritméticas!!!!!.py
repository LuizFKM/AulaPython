print('Programa de operações aritméticas!!!!!!!!!!!!!!')
x = int(input('Digite o primeiro número: '))
y = int(input('Digite o segundo número: '))

soma = (x + y)
print('A soma é: ', soma) 

sub = (x - y)
print('A subtração é: ', sub) 

mult = (x * y)
print('A multiplicação é: ', mult) 

div = (x / y)
print('A divisão é: ', div)

resto = (x % y)
print('O resto da divisão entre os dois números é: ', resto)

exp = (x ** y)
print('O número o primeiro número elevado ao segundo é: ', exp)

divExata = (x // y)
print('A divisão exata entre os números é: ', divExata)




